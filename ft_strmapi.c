#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int	i;
	char	*r;

	if (!s)
		return (NULL);
	r = malloc(sizeof(char) * (ft_strlen(s) + 1));
	if (!s)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		r[i] = f(i, s[i]);
		i++;
	}
	r[i] = '\0';
	return (r);
}
