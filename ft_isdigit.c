int	ft_isdigit(char *str)
{
	int	i;
	
	i = 1;
	while (*str != '\0')
	{
		if (!(*str >= '0' && *str <= '9'))
		{
			i = 0;
			break ;
		}
		str++;
	}
	return (i);
}
