int	ft_isalpha(char *str)
{
	int	i;
	char	c;

	i = 1;
	while (*str != '\0')
	{
		c = *str;
		if (!((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z')))
		{
			i = 0;
			break ;
		}
		str++;
	}
	return (i);
}
